    private void tongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tongBtnActionPerformed
        // TODO add your handling code here:
        int sum = 0;
        for (String item : items) {
            int num = Integer.parseInt(item);
            sum += num;
        }
        JOptionPane.showMessageDialog(this, "Tổng giá trị trong jList: " + sum, "Tính Tổng", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_tongBtnActionPerformed

