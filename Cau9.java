    private void todenleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_todenleBtnActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < items.size(); i++) {
            int num = Integer.parseInt(items.get(i));
            if (num % 2 == 1 || num % -2 == 1) {
                list.addSelectionInterval(i, i);
            }
        }
    }//GEN-LAST:event_todenleBtnActionPerformed

    private void todenngtoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_todenngtoBtnActionPerformed
        // TODO add your handling code here:
         for (int i = 0; i < items.size(); i++) {
            int num = Integer.parseInt(items.get(i));
            if (isPrime(num)) {
                list.addSelectionInterval(i, i);
            }
        }
    }//GEN-LAST:event_todenngtoBtnActionPerformed

    private void nhapBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nhapBtnActionPerformed
        // TODO add your handling code here
       String text = nhapText.getText();
    try {
        int num = Integer.parseInt(text);
        if (soamCheck.isSelected() || num >= 0) {
            items.add(text);
            list.setListData(items);
            nhapText.setText("");
        } else {
            JOptionPane.showMessageDialog(this,
                "Please check to allow negative numbers or enter a non-negative number.",
                "Message",
                JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
            "Please enter a valid integer.",
            "Message",
            JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_nhapBtnActionPerformed

